def normalize_cnpj(c):
    """Remove tudo que não é dígito"""
    return re.sub(r"[^\d]", "", str(c).strip())

def main():
    # Parsear CNPJs do usuário
    user_cnpjs = set()
    for line in cnpjs_usuario.strip().split("\n"):
        line = line.strip()
        if line:
            norm = normalize_cnpj(line)
            if len(norm) >= 11:  # CNPJ ou CPF
                user_cnpjs.add(norm)

    print(f"CNPJs na lista do usuário: {len(user_cnpjs)}")

    # Abrir V22
    print("Abrindo V22 (read_only)...", flush=True)
    wb = openpyxl.load_workbook(V22, read_only=True, data_only=True)

    # === CARTEIRA ===
    ws_cart = wb["CARTEIRA"]
    # Col B (2) = CNPJ, Col N (14) = SITUAÇÃO, Col L (12) = CONSULTOR
    # Col P (16) = DIAS SEM COMPRA, Col Z-AJ (26-36) = VENDAS meses
    # Col AM (39) = CURVA ABC

    cart_data = {}  # cnpj_norm → {row, nome, situacao, consultor, dias_sem_compra, vendas_total, curva}
    print("Lendo CARTEIRA...", flush=True)
    row_num = 3
    for row in ws_cart.iter_rows(min_row=4, max_col=62, values_only=True):
        row_num += 1
        cnpj_raw = row[1]  # col B
        if not cnpj_raw:
            continue
        cnpj_norm = normalize_cnpj(cnpj_raw)
        if not cnpj_norm or len(cnpj_norm) < 11:
            continue

        nome = row[0] or ""  # col A
        situacao = row[13] or ""  # col N
        consultor = row[11] or ""  # col L
        dias_sem = row[15]  # col P
        curva = row[38] or ""  # col AM

        # Vendas total (col Z = 26, index 25)
        vendas_total = row[25] if len(row) > 25 else None

        cart_data[cnpj_norm] = {
            "row": row_num,
            "nome": str(nome)[:40],
            "situacao": str(situacao),
            "consultor": str(consultor),
            "dias_sem_compra": dias_sem,
            "vendas_total": vendas_total,
            "curva": str(curva),
        }

    print(f"CARTEIRA: {len(cart_data)} CNPJs lidos")

    # === DRAFT 1 (vendas Mercos) ===
    ws_d1 = wb["DRAFT 1"]
    draft1_data = {}  # cnpj_norm → {vendas_cols...}
    print("Lendo DRAFT 1...", flush=True)
    d1_row = 2
    for row in ws_d1.iter_rows(min_row=3, max_col=45, values_only=True):
        d1_row += 1
        cnpj_raw = row[1]  # col B
        if not cnpj_raw:
            continue
        cnpj_norm = normalize_cnpj(cnpj_raw)
        if not cnpj_norm or len(cnpj_norm) < 11:
            continue
        draft1_data[cnpj_norm] = {
            "row": d1_row,
            "nome": str(row[0] or "")[:40],
        }

    print(f"DRAFT 1: {len(draft1_data)} CNPJs lidos")
    wb.close()

    # === CRUZAMENTO ===
    print(f"\n{'='*120}")
    print(f"CRUZAMENTO: {len(user_cnpjs)} CNPJs do usuário")
    print(f"{'='*120}")

    found_cart = 0
    found_d1 = 0
    not_in_cart = []
    not_in_d1 = []

    ativos = []
    inativos = []
    prospects_found = []

    for cnpj in sorted(user_cnpjs):
        in_cart = cnpj in cart_data
        in_d1 = cnpj in draft1_data

        if in_cart:
            found_cart += 1
            d = cart_data[cnpj]
            sit = d["situacao"].upper()
            if "PROSPECT" in sit:
                prospects_found.append((cnpj, d))
            elif "INAT" in sit:
                inativos.append((cnpj, d))
            else:
                ativos.append((cnpj, d))
        else:
            not_in_cart.append(cnpj)

        if in_d1:
            found_d1 += 1
        else:
            not_in_d1.append(cnpj)

    # Resumo
    print(f"\n--- RESUMO ---")
    print(f"  Na CARTEIRA:    {found_cart}/{len(user_cnpjs)}")
    print(f"  No DRAFT 1:     {found_d1}/{len(user_cnpjs)}")
    print(f"  ATIVOS:         {len(ativos)}")
    print(f"  INATIVOS:       {len(inativos)}")
    print(f"  PROSPECTS:      {len(prospects_found)}")
    print(f"  NÃO encontrados CARTEIRA: {len(not_in_cart)}")
    print(f"  NÃO encontrados DRAFT 1:  {len(not_in_d1)}")

    # Detalhe ATIVOS
    print(f"\n{'='*120}")
    print(f"ATIVOS ({len(ativos)}):")
    print(f"{'CNPJ':20s} {'NOME':40s} {'SITUAÇÃO':15s} {'CONSULTOR':20s} {'DIAS S/C':>8s} {'CURVA':>6s}")
    print("-" * 120)
    for cnpj, d in sorted(ativos, key=lambda x: x[1]["nome"]):
        dias = str(d["dias_sem_compra"] or "")[:8]
        print(f"{cnpj:20s} {d['nome']:40s} {d['situacao']:15s} {d['consultor']:20s} {dias:>8s} {d['curva']:>6s}")

    # Detalhe INATIVOS
    if inativos:
        print(f"\n{'='*120}")
        print(f"INATIVOS ({len(inativos)}):")
        print(f"{'CNPJ':20s} {'NOME':40s} {'SITUAÇÃO':15s} {'CONSULTOR':20s} {'DIAS S/C':>8s} {'CURVA':>6s}")
        print("-" * 120)
        for cnpj, d in sorted(inativos, key=lambda x: x[1]["nome"]):
            dias = str(d["dias_sem_compra"] or "")[:8]
            print(f"{cnpj:20s} {d['nome']:40s} {d['situacao']:15s} {d['consultor']:20s} {dias:>8s} {d['curva']:>6s}")

    # Detalhe PROSPECTS (não deveriam estar na lista se são só ativos/inativos)
    if prospects_found:
        print(f"\n{'='*120}")
        print(f"PROSPECTS encontrados na lista ({len(prospects_found)}) — estes NÃO são clientes ativos:")
        for cnpj, d in prospects_found:
            print(f"  {cnpj} - {d['nome']}")

    # NÃO encontrados
    if not_in_cart:
        print(f"\n{'='*120}")
