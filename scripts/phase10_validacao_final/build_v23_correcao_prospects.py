    print(f"  Fórmulas adicionadas: {formulas_added:,}")

    # Corrigir ranges $557 → $6147 nas fórmulas recém-adicionadas
    range_fixes = 0
    for cnpj_norm, r, mercos_info in prospects_to_fix:
        if r >= first_prospect_row:
            for col_num in formula_cols.keys():
                v = ws_cart.cell(row=r, column=col_num).value
                if isinstance(v, str) and "$557" in v:
                    ws_cart.cell(row=r, column=col_num, value=v.replace("$557", "$6147"))
