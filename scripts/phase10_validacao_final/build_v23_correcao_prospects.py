def normalize_cnpj(c):
    return re.sub(r"[^\d]", "", str(c).strip())


def timer(start, step, total_steps):
    """Retorna string com tempo decorrido e estimativa de conclusão"""
    elapsed = time.time() - start
    if step > 0:
        per_step = elapsed / step
        remaining = per_step * (total_steps - step)
        return f"[{elapsed:.0f}s | ~{remaining:.0f}s restantes | {step}/{total_steps}]"
    return f"[{elapsed:.0f}s]"


def main():
    t0 = time.time()
    TOTAL_STEPS = 7
    current_step = 0

    print("=" * 100)
