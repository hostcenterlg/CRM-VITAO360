    print(f"  Tamanho: {size_mb:.2f} MB (V22 era {v22_size:.2f} MB)")
    print(f"")
    print(f"  CORREÇÕES APLICADAS:")
    print(f"    SITUAÇÃO corrigida:      {fixed_sit} prospects → NOVO")
    print(f"    Fórmulas adicionadas:    {formulas_added:,} (ex-prospects agora com fórmulas)")
